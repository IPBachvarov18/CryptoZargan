var currentLanguage = "en";

/**
 * Replaces the text on the page with the chosen language.
 *
 * @param {string} item The item that has to be replaced.
 * @param {string} text The text that the item is going to be replaced with.
 */
function replaceElementText(item, text) {
	if (!item.is("button")) {
		item.html(text);
	} else {
		item.text(text);
	}
}

/**
 * Translates the elements with the data-lang attribute.
 *
 * @param {string} langId The identificator of the language (bg/en).
 */
function translateLabel(langId) {
	console.log(langId);
	currentLanguage = langId;

	// Gets all tags that have 'data-lang' attribute present
	$("[data-lang]").each(function () {
		let item = $(this);
		if (
			textTranslation.hasOwnProperty(item.data("lang")) &&
			textTranslation[item.data("lang")].hasOwnProperty(langId)
		) {
			let text = textTranslation[item.data("lang")][langId];
			replaceElementText(item, text);
		} else {
			replaceElementText(
				item,
				"<font color='red'>" + item.data("lang") + "</font>"
			);
		}
	});
}

/**
 * Gets the translated elements.
 *
 * @param {string} elementId The element's Id.
 * @return {string} textTranslation The translated text.
 */
function getTranslatedText(elementId) {
	return textTranslation[elementId][currentLanguage];
}
